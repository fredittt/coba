public class Siswa {
    String nama;
    String kelas;
    String alamat;

    public String getNama(){
        return nama;
    }
    public void setNama (String nama){
        this.nama = nama;
    }
    public String getKelas(){
        return kelas;
    }
    public void setKelas (String kelas){
        this.kelas = kelas;
    }
    public String getAlamat(){
        return alamat;
    }
    public void setAlamat (String alamat){
        this.alamat = alamat;
    }
}
